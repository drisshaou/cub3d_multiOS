#include "cub3d.h"

void	init_bufz(t_c3d *t)
{
	int	i;

	if (!(t->bufz = malloc(sizeof(double) * t->winw)))
		ft_exit(t);
	i = -1;
	while (++i < t->winw)
		t->bufz[i] = 0.0;
}
