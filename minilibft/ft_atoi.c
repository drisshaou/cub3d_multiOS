#include "minilibft.h"

int	ft_atoi(const char *s)
{
	int	r;

	r = 0;
	while (*s == 32 || (*s >= 9 && *s <= 13))
		s++;
	while (*s >= '0' && *s <= '9')
	{
		r = (r * 10) + (*s - '0');
		s++;
	}
	return (r);
}
